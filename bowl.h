int swap_yz(){
    FILE *out;
    if (!(out = fopen("../new_bowl.gl", "w")))
        return(-1);
    GLfloat tmp;
    for(int i=0; i < 315; ++i){
        int v0, v1, v2;
        v0 = i * 8 + 5;
        v1 = v0 + 1;
        v2 = v1 + 1;
        tmp = GL3DS_VERTEX_bowl[v1];
        GL3DS_VERTEX_bowl[v1] = GL3DS_VERTEX_bowl[v2];
        GL3DS_VERTEX_bowl[v2] = tmp;
    }
    if (fwrite(GL3DS_VERTEX_bowl, 10080, 1, out) != 1)
        return(-1);
    fclose(out);
    return 0;
}

int GL3DS_initialize_bowl() {
  int ReturnVal;
  FILE *in;
  if (!(in = fopen("../new_bowl.gl", "rb")))
    return(-1);
  if (fread(GL3DS_VERTEX_bowl, 10080, 1, in) != 1)
    return(-1);
  fclose(in);
//  swap_yz();
  ReturnVal = glGenLists(1);
  glInterleavedArrays(GL_T2F_N3F_V3F, 0, GL3DS_VERTEX_bowl);
  glNewList(ReturnVal, GL_COMPILE);
    glFrontFace(GL_CCW);
    glEnable(GL_CULL_FACE);
    glCullFace(GL_BACK);
    glPolygonMode(GL_FRONT_AND_BACK, GL_LINE); // set both face fill_mode (wrt GL_LINE GL_POINT)
    glMaterialfv(GL_FRONT, GL_AMBIENT, (GLfloat *) &GL3DS_MATERIAL_bowl[0]);
    glMaterialfv(GL_FRONT, GL_DIFFUSE, (GLfloat *) &GL3DS_MATERIAL_bowl[4]);
    glMaterialfv(GL_FRONT, GL_SPECULAR, (GLfloat *) &GL3DS_MATERIAL_bowl[8]);
    glDrawElements(GL_TRIANGLES, 3276, GL_UNSIGNED_INT, &GL3DS_INDEX_bowl[0]);
  glEndList();
  print_vertex_bowl();
  return (ReturnVal);
}
